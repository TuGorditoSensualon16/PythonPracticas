
#Calcular la velocidad de un objeto teniendo las variables de distacioa y tiempo 

#ingresar la distancia 
distacia = float(input("ingrese la distancia: "))
tiempo = float(input("ingrese el tiempo: "))
#calcular la velocidad
velocidad = distacia/tiempo
#mostrar el resultado
print("la velocidad aproximada del objeto es: ", velocidad, "metros/segundo")

 
